package Homeworkweek8;

public class V15LeftAngleTriangle {
    public static void main(String[] args) {
        int rows = 5;

        for (int x =1; x <= rows; x++)
        {
            for (int i = 1; i <= x; i++)
            {
                System.out.print(" *");
            }

            System.out.println("");
        }
    }
}






